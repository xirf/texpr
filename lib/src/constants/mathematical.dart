/// Famous mathematical constants.
library;

import 'dart:math' as math;

/// e - Euler's number, base of natural logarithm
const double e = math.e;

/// φ (phi) - Golden ratio (1 + √5) / 2
final double phi = (1 + math.sqrt(5)) / 2;

/// γ (gamma) - Euler-Mascheroni constant
const double gamma = 0.5772156649015329;

/// Ω - Omega constant, solution to Ω·e^Ω = 1
const double omega = 0.5671432904097838;

/// δ_S (delta) - Silver ratio (1 + √2)
final double delta = 1 + math.sqrt(2);

/// ζ(3) - Apéry's constant
const double zeta3 = 1.2020569031595943;

/// G - Gravitational constant (m³/(kg·s²))
const double gravitationalConstant = 6.67430e-11;

/// Infinity
const double infty = double.infinity;

/// ℏ (hbar) - Reduced Planck constant (J·s)
const double hbar = 1.054571817e-34;
